def get_adjusted_revenue_share_rate(initial_year, project_length, revenue_share_rate):
    """
    Get revenue share rate for each year of the project.
    Handles projects starting before or after 2021, and prevents index errors.
    """
    adjusted_rs_rate = []
    
    if not revenue_share_rate:
        # Default rates if none provided
        revenue_share_rate = settings.REVENUE_SHARE_RATE
    
    # hardcoded value alert
    if initial_year >= 2021:
        for i in range(initial_year, initial_year + project_length):
            # Calculate index with bounds checking
            index = (i - 2021) // 5
            index = min(index, len(revenue_share_rate) - 1)  # Prevent index error
            adjusted_rs_rate.append(revenue_share_rate[index])
    else:
        # For projects starting before 2021, use the earliest rate
        adjusted_rs_rate = [revenue_share_rate[0] for _ in range(project_length)]
    
    return adjusted_rs_rate


def total_cashflow_rs(revenue_share_rate, megawatts, year, project_length):
    """
    Calculate revenue share cashflow
    :revenue_share_rate: array of revenue rates
    :megawatts: Size of solar project
    :year: Starting year of the solar project
    :return: List of revenue share revenue generated during each year, nominal $
    """
    cashflow_rev = []
    for i in range(project_length):
        cashflow_rev.append(revenue_share_rate[i] * megawatts)
    return cashflow_rev


def total_adj_rev(cas, discount_rate, initial_year):
    """
    Calculate present value of revenue stream
    :cas: List of revenue generated during each year, nominal $
    :discount_rate: rate at which revenue values will be discounted
    :initial_year: Starting year of project
    :return: List of revenue generated during each year in present value
    """
    tot = []
    for i in range(len(cas)):
        if discount_rate == 0:
            tot.append(cas[i])
        else:
            tot.append(cas[i] / ((1 + discount_rate) ** (i + (initial_year - settings.DISCOUNTING_BASE_YEAR))))
    return tot


def apply_assessment_cycle(values, years_between_assessment):
    """
    Apply assessment cycle to values.
    Values are recalculated at assessment intervals, held constant between.
    """
    if not values:
        return []
    
    adjusted_values = []
    for i in range(len(values)):
        if i % years_between_assessment == 0:
            adjusted_values.append(values[i])
        else:
            adjusted_values.append(adjusted_values[i - 1])
    
    return adjusted_values