import math


def depreciation_ext(depreciation_schedule):
    """
    Takes in schedule of effective rates
    Returns extended list with final effective rate to be available for calculations out to 35 years
    Does NOT modify original list
    """
    extended = depreciation_schedule.copy() if depreciation_schedule else []
    if not extended:
        return extended
    
    last_rate = extended[-1]
    while len(extended) < 35:
        extended.append(last_rate)
    
    return extended


def effective_rate_ext(effective_rate_list, project_length):
    """
    Takes in schedule of effective rates
    Returns extended list with final effective rate available for project length
    Does NOT modify original list
    """
    extended = effective_rate_list.copy() if effective_rate_list else []
    if not extended:
        return extended
    
    last_rate = extended[-1]
    while len(extended) <= project_length:
        extended.append(last_rate)
    
    return extended


