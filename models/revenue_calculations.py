def increase_in_local_contribution(pv_required_education_contribution, 
                                  baseline_required_education_contribution):
    """Calculate increase in education contribution"""
    increase_contribution = []
    for i in range(len(pv_required_education_contribution)):
        increase_contribution.append(
            pv_required_education_contribution[i] - baseline_required_education_contribution[i]
        )
    return increase_contribution


def pv_required_education_contribution(locality_education_budget, budget_escalator, 
                                      pv_composite_index):
    """Calculate project-value required education contribution"""
    pv_education_contribution = []
    for i in range(len(pv_composite_index)):
        # The budget escalator value in the database is divided by 100 in views.performCalculations 
        # before being passed to this function, so we do not divide by 100 in the calculation below.
        contribution = locality_education_budget * ((1 + budget_escalator) ** i) * pv_composite_index[i]
        pv_education_contribution.append(contribution)
    return pv_education_contribution


def baseline_required_education_contribution(locality_education_budget, budget_escalator, 
                                           baseline_composite_index):
    """Calculate baseline required education contribution"""
    baseline_education_contribution = []
    for i in range(len(baseline_composite_index)):
        contribution = locality_education_budget * ((1 + budget_escalator) ** i) * baseline_composite_index[i]
        baseline_education_contribution.append(contribution)
    return baseline_education_contribution


