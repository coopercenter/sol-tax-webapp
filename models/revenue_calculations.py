def depreciation_ext(depreciation_schedule):
    """
    Takes in schedule of effective rates
    Returns extended list with final effective rate to be available for calculations out to 35 years
    Does NOT modify original list
    """
    # Handle string representation of list (from database)
    
    if isinstance(depreciation_schedule, str):
        try:
            depreciation_schedule = [float(x) for x in depreciation_schedule.strip('{}').split(',')]
        except (ValueError, TypeError):
            depreciation_schedule = []
    extended = depreciation_schedule.copy() if depreciation_schedule else []
    if not extended:
        return extended
    
    last_rate = extended[-1]
    # hardcoded value alert - 35 year extension for depreciation schedule, should be sufficient for all current projects modeled but may want to make more dynamic in the future
    while len(extended) < settings.MAX_DEPRECIATION_SCHEDULE_LENGTH:
        extended.append(last_rate)
    return extended


def effective_rate_ext(effective_rate_list, project_length):
    """
    Takes in schedule of effective rates
    Returns extended list with final effective rate available for project length
    Does NOT modify original list
    """
       # Handle string representation of list (from database)
    if isinstance(effective_rate_list, str):
        try:
            effective_rate_list = json.loads(effective_rate_list)
        except (json.JSONDecodeError, TypeError):
            effective_rate_list = []
    extended = effective_rate_list.copy() if effective_rate_list else []
    if not extended:
        return extended
    
    last_rate = extended[-1]
    while len(extended) <= project_length:
        extended.append(last_rate)
    
    return extended


