def get_true_values_adm(local_true_values, divisional_adm, statewide_total_true_values, 
                       total_state_adm):
    """Calculate true values indexed by ADM"""
    final_true_values = []
    for i in range(len(local_true_values)):
        if divisional_adm == 0 or total_state_adm == 0:
            final_true_values.append(0)
            continue
        numerator = local_true_values[i] / divisional_adm
        denominator = statewide_total_true_values[i] / total_state_adm
