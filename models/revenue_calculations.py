def current_land_value(total_acreage, baseline_value, starting_year, project_length, 
                      years_between_assessment,property_appreciation_schedule):
    """Calculate land value for each year in project_length with annual appreciation at ANNUAL_PROPERTY_APPRECIATION_RATE% """
    baseline = []
    total_acreage_value = total_acreage * baseline_value
    for i in range(project_length):
        if i % years_between_assessment == 0:
            baseline.append(round(total_acreage_value * property_appreciation_schedule[i]))
        else:
            baseline.append(baseline[i - 1])
    return baseline


def new_land_value(total_acreage, inside_acreage, outside_acreage, inside_fence_value, 
                  outside_fence_value, starting_year, project_length, years_between_assessment,
                  property_appreciation_schedule):
    """Calculate new land value with solar project"""
    total = []
    inside_fence_total_value = inside_acreage * inside_fence_value
    outside_fence_total_value = outside_acreage * outside_fence_value
    for i in range(project_length):
        if i % years_between_assessment == 0:
            # ANNUAL_PROPERTY_APPRECIATION_RATE - 1.2% annual property appreciation rate
            inside = inside_fence_total_value * property_appreciation_schedule[i]
            outside = outside_fence_total_value * property_appreciation_schedule[i]
            total.append(round(inside + outside))
        else:
            total.append(total[i - 1])
    return total


def increase_in_land_value(current_land_value, new_land_value, assessment_ratio):
    """Calculate difference between new and current land values"""
    difference = []
    for i in range(len(current_land_value)):
        difference.append(round((new_land_value[i] - current_land_value[i]) * assessment_ratio))
    return difference


