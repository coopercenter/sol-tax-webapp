def get_mt_exemption(size_mw, mt_exemption):
    """
    Get the exemption rate for the project. 
    If above 150 MW, no exemptions applied. Otherwise use M&T stepdown exemption rate
    """
    if size_mw > 150:
        return 0
    else:
        return mt_exemption


def get_mt_tax_rate(size_mw, mt_tax_rate, real_estate_rate):
    """
    Get the M&T tax rate for the project. 
    If above 25 MW use real estate rate, otherwise use mt_tax_rate
    """
    if size_mw > 25:
        return real_estate_rate
    else:
        return mt_tax_rate


