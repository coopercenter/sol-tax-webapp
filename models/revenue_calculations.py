def composite_index(adm_composite_index, per_capita_composite_index):
    """Calculate final composite index (2/3 ADM, 1/3 per capita, scaled by 0.45)
     Document the calculations."""
    final_pv_composite_index = []
    for i in range(len(adm_composite_index)):
        # hardcoded values alert
        local_composite_index = ((settings.LOCAL_COMPOSITE_INDEX_SHARES['adm_share'] * adm_composite_index[i]) +
        (settings.LOCAL_COMPOSITE_INDEX_SHARES['population_share'] * per_capita_composite_index[i]))
        value = settings.LOCAL_COMPOSITE_INDEX_SCALE_FACTOR * local_composite_index
        # Why the scaling time 10,000? 
        # Suggested reason: This is to preserve 4 decimal places in the final composite index while avoiding floating point precision issues that 
        # can arise with very small decimal numbers. By multiplying by 10,000 before truncating, we can keep 4 decimal places of precision without running into issues where the number becomes too small to represent accurately in Python's floating point arithmetic. After truncating, we divide by 10,000 to return to the original scale.
        factor = 10.0 ** 4
        final_pv_composite_index.append(math.trunc((value * factor)) / factor)
    return final_pv_composite_index


def adm_composite_index(true_values_adm, gross_income_adm, retail_sales_adm):
    """Calculate ADM composite index (50% true value, 40% income, 10% sales)"""
    #print(f'Calculating ADM composite index:\n')
