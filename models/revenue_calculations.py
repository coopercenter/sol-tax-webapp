###############################################################################


def solar_facility_valuation(year, investment, mt_exemption, depreciation_schedule, 
                             assessment_ratio, project_length):
    """Calculate solar facility valuation over project lifetime"""
    valuation = []
    for i in range(project_length):
        after_exemption = investment * (1 - mt_exemption[i])
        after_depreciation = after_exemption * depreciation_schedule[i]
        assessed_facility_valuation = after_depreciation * assessment_ratio
        valuation.append(round(assessed_facility_valuation))
    return valuation


